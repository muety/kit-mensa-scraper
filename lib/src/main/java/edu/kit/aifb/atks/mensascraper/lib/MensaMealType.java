/**
 * Enum representing different types of meals.
 */
public enum MensaMealType {
    /** No specific or unknown type of meal */
    NONE,
    /** A meal containing pork meat */
    PORK,
    /** A meal containing beef meat */
    BEEF,
    /** A meal containing fish */
    MSC,
    /** A vegetarian meal */
    VEGETARIAN,
    /** A vegan meal */
    VEGAN
}
